import java.util.Scanner;

public class ForDemo{

public static void main(String args[])

  {
	  int i;
	  System.out.println("Even \t Odd");
    for(i=0;i<=10;i++)
	 {
	    if(i%2==0){
			//System.out.print("Even ");
			System.out.println( i );
		}
	
		else
		{
			//System.out.print("Odd ");
			System.out.println("\t" + i);
		}
	 }	
	  }
   }
